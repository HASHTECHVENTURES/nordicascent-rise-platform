-- Allow Word CV uploads in the documents bucket (PDF was already allowed).

UPDATE storage.buckets
SET allowed_mime_types = ARRAY[
  'application/pdf',
  'application/msword',
  'application/vnd.openxmlformats-officedocument.wordprocessingml.document',
  'image/jpeg',
  'image/png'
]::text[]
WHERE id = 'documents';
